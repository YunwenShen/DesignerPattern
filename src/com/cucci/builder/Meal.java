package com.cucci.builder;

/**
 * 具体产品类
 *
 * @author shenyw
 **/
public class Meal {

    /**
     * 食物
     */
    private String food;

    /**
     * 饮料
     */
    private String drink;

    public String getFood() {
        return food;
    }

    public void setFood(String food) {
        this.food = food;
    }

    public String getDrink() {
        return drink;
    }

    public void setDrink(String drink) {
        this.drink = drink;
    }
}
